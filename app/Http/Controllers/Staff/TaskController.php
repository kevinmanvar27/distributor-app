<?php

namespace App\Http\Controllers\Staff;

use App\Http\Controllers\Controller;
use App\Models\Task;
use App\Models\TaskComment;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Validator;

class TaskController extends Controller
{
    /**
     * Display a listing of tasks assigned to the logged-in staff
     */
    public function index(Request $request)
    {
        // Check permission
        if (!Auth::user()->hasPermission('view_tasks')) {
            abort(403, 'Unauthorized action.');
        }

        $user = Auth::user();
        
        $query = Task::with(['assignedBy', 'assignedTo'])
            ->where('assigned_to', $user->id)
            ->orderBy('created_at', 'desc');

        // Filter by status
        if ($request->has('status') && $request->status !== '') {
            $query->where('status', $request->status);
        }

        // Search
        if ($request->has('search') && $request->search !== '') {
            $search = $request->search;
            $query->where(function($q) use ($search) {
                $q->where('title', 'like', "%{$search}%")
                  ->orWhere('description', 'like', "%{$search}%");
            });
        }

        $tasks = $query->paginate(15);
        $statuses = Task::getStatuses();

        // Get statistics for the staff
        $stats = [
            'total' => Task::where('assigned_to', $user->id)->count(),
            'pending' => Task::where('assigned_to', $user->id)->pending()->count(),
            'in_progress' => Task::where('assigned_to', $user->id)->inProgress()->count(),
            'with_questions' => Task::where('assigned_to', $user->id)->withQuestions()->count(),
            'done' => Task::where('assigned_to', $user->id)->done()->count(),
            'verified' => Task::where('assigned_to', $user->id)->verified()->count(),
        ];

        // Return JSON for API requests
        if ($request->expectsJson() || $request->is('api/*')) {
            return response()->json([
                'success' => true,
                'tasks' => $tasks,
                'statuses' => $statuses,
                'statistics' => $stats,
            ]);
        }

        // Return view for web requests
        return view('staff.tasks.index', compact('tasks', 'statuses', 'stats'));
    }

    /**
     * Display the specified task
     */
    public function show($id)
    {
        // Check permission
        if (!Auth::user()->hasPermission('view_tasks')) {
            abort(403, 'Unauthorized action.');
        }

        $user = Auth::user();
        
        $task = Task::with(['assignedBy', 'assignedTo', 'comments.user'])
            ->where('assigned_to', $user->id)
            ->findOrFail($id);

        // Return JSON for API requests
        if (request()->expectsJson() || request()->is('api/*')) {
            return response()->json([
                'success' => true,
                'task' => $task,
            ]);
        }

        // Return view for web requests
        return view('staff.tasks.show', compact('task'));
    }

    /**
     * Update task status
     */
    public function updateStatus(Request $request, $id)
    {
        // Check permission
        if (!Auth::user()->hasPermission('update_task_status')) {
            abort(403, 'Unauthorized action.');
        }

        $user = Auth::user();
        
        $task = Task::where('assigned_to', $user->id)->findOrFail($id);

        // Prevent changes to verified tasks
        if ($task->status === Task::STATUS_VERIFIED) {
            // Return JSON for API requests
            if ($request->expectsJson() || $request->is('api/*')) {
                return response()->json([
                    'success' => false,
                    'message' => 'Cannot update status. This task is verified and locked.',
                ], 403);
            }

            // Redirect for web requests
            return redirect()->route('staff.tasks.show', $task->id)
                ->with('error', 'Cannot update status. This task is verified and locked.');
        }

        $validator = Validator::make($request->all(), [
            'status' => 'required|in:in_progress,question,done',
        ]);

        if ($validator->fails()) {
            return response()->json([
                'success' => false,
                'message' => 'Validation failed',
                'errors' => $validator->errors(),
            ], 422);
        }

        // Staff can only update to certain statuses
        $allowedStatuses = [Task::STATUS_IN_PROGRESS, Task::STATUS_QUESTION, Task::STATUS_DONE];
        if (!in_array($request->status, $allowedStatuses)) {
            return response()->json([
                'success' => false,
                'message' => 'Invalid status transition',
            ], 403);
        }

        $oldStatus = $task->status;
        $task->status = $request->status;
        $task->save();

        // Return JSON for API requests
        if ($request->expectsJson() || $request->is('api/*')) {
            return response()->json([
                'success' => true,
                'message' => 'Task status updated successfully',
                'task' => $task->load(['assignedBy', 'assignedTo']),
            ]);
        }

        // Redirect for web requests
        return redirect()->route('staff.tasks.show', $task->id)
            ->with('success', 'Task status updated successfully');
    }

    /**
     * Add a comment to the task
     */
    public function addComment(Request $request, $id)
    {
        // Check permission
        if (!Auth::user()->hasPermission('update_task_status')) {
            abort(403, 'Unauthorized action.');
        }

        $user = Auth::user();
        
        $task = Task::where('assigned_to', $user->id)->findOrFail($id);

        // Prevent comments on verified tasks
        if ($task->status === Task::STATUS_VERIFIED) {
            // Return JSON for API requests
            if ($request->expectsJson() || $request->is('api/*')) {
                return response()->json([
                    'success' => false,
                    'message' => 'Cannot add comment. This task is verified and locked.',
                ], 403);
            }

            // Redirect for web requests
            return redirect()->route('staff.tasks.show', $task->id)
                ->with('error', 'Cannot add comment. This task is verified and locked.');
        }

        $validator = Validator::make($request->all(), [
            'comment' => 'required|string',
        ]);

        if ($validator->fails()) {
            return response()->json([
                'success' => false,
                'message' => 'Validation failed',
                'errors' => $validator->errors(),
            ], 422);
        }

        $comment = TaskComment::create([
            'task_id' => $task->id,
            'user_id' => $user->id,
            'comment' => $request->comment,
        ]);

        $comment->load('user');

        // Return JSON for API requests
        if ($request->expectsJson() || $request->is('api/*')) {
            return response()->json([
                'success' => true,
                'message' => 'Comment added successfully',
                'comment' => $comment,
            ], 201);
        }

        // Redirect for web requests
        return redirect()->route('staff.tasks.show', $task->id)
            ->with('success', 'Comment added successfully');
    }

    /**
     * Get task statistics for the staff
     */
    public function statistics()
    {
        // Check permission
        if (!Auth::user()->hasPermission('view_tasks')) {
            abort(403, 'Unauthorized action.');
        }

        $user = Auth::user();
        
        $stats = [
            'total' => Task::where('assigned_to', $user->id)->count(),
            'pending' => Task::where('assigned_to', $user->id)->pending()->count(),
            'in_progress' => Task::where('assigned_to', $user->id)->inProgress()->count(),
            'with_questions' => Task::where('assigned_to', $user->id)->withQuestions()->count(),
            'done' => Task::where('assigned_to', $user->id)->done()->count(),
            'verified' => Task::where('assigned_to', $user->id)->verified()->count(),
        ];

        return response()->json([
            'success' => true,
            'statistics' => $stats,
        ]);
    }
}
